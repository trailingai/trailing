fn table_row_height(values: &[String], columns: &[TableColumn], font: FontFace) -> f32 {
    let max_lines = columns
        .iter()
        .enumerate()
        .map(|(index, column)| {
            wrap_for_width(
                values.get(index).map_or("", String::as_str),
                column.width - CELL_PADDING_X * 2.0,
                font,
                TABLE_FONT_SIZE,
            )
            .len()
        })
        .max()
        .unwrap_or(1)
        .max(1);

    CELL_PADDING_Y * 2.0 + max_lines as f32 * TABLE_LINE_HEIGHT
}

fn rows_or_placeholder(rows: Vec<Vec<String>>, width: usize, message: &str) -> Vec<Vec<String>> {
    if !rows.is_empty() {
        return rows;
    }

    let mut placeholder = vec![String::new(); width];
    placeholder[0] = message.to_string();
    vec![placeholder]
}

fn gap_rows(gaps: &[ComplianceGap]) -> Vec<Vec<String>> {
    if gaps.is_empty() {
        return rows_or_placeholder(Vec::new(), 4, "No gaps identified.");
    }

    gaps.iter()
        .map(|gap| {
            vec![
                gap.gap_id.clone(),
                severity_label(&gap.severity).to_string(),
                gap.remediation_owner
                    .clone()
                    .unwrap_or_else(|| "unassigned".to_string()),
                gap.description.clone(),
            ]
        })
        .collect()
}

fn join_or_dash(values: &[String]) -> String {
    if values.is_empty() {
        "-".to_string()
    } else {
        values.join(", ")
    }
}

fn package_time_range(package: &EvidencePackage) -> Option<(DateTime<Utc>, DateTime<Utc>)> {
    let mut timestamps = package
        .actions
        .iter()
        .map(|action| action.timestamp)
        .chain(package.oversight_events.iter().map(|event| event.timestamp));
    let first = timestamps.next()?;

    let mut min_timestamp = first;
    let mut max_timestamp = first;

    for timestamp in timestamps {
        if timestamp < min_timestamp {
            min_timestamp = timestamp;
        }
        if timestamp > max_timestamp {
            max_timestamp = timestamp;
        }
    }

    Some((min_timestamp, max_timestamp))
}

fn format_timestamp(timestamp: DateTime<Utc>) -> String {
    timestamp.format("%Y-%m-%d %H:%M:%S UTC").to_string()
}

fn format_time_range(start: DateTime<Utc>, end: DateTime<Utc>) -> String {
    if start == end {
        return format_timestamp(start);
    }

    format!("{} to {}", format_timestamp(start), format_timestamp(end))
}

