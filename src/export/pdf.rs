fn wrap_for_width(value: &str, width: f32, font: FontFace, font_size: f32) -> Vec<String> {
    wrap_text(value, approx_chars_for_width(width, font, font_size))
}

fn approx_chars_for_width(width: f32, font: FontFace, font_size: f32) -> usize {
    ((width / (font_size * font.width_factor())).floor() as usize).max(1)
}

fn wrap_text(value: &str, width: usize) -> Vec<String> {
    let sanitized = sanitize_text(value);
    if sanitized.is_empty() {
        return vec![String::new()];
    }

    let mut lines = Vec::new();
    let mut current = String::new();

    for word in sanitized.split_whitespace() {
        if current.is_empty() {
            if word.len() <= width {
                current.push_str(word);
            } else {
                lines.extend(split_long_word(word, width));
            }
            continue;
        }

        if current.len() + 1 + word.len() <= width {
            current.push(' ');
            current.push_str(word);
            continue;
        }

        lines.push(current);
        current = String::new();

        if word.len() <= width {
            current.push_str(word);
        } else {
            lines.extend(split_long_word(word, width));
        }
    }

    if !current.is_empty() {
        lines.push(current);
    }

    if lines.is_empty() {
        lines.push(String::new());
    }

    lines
}

fn split_long_word(word: &str, width: usize) -> Vec<String> {
    word.chars()
        .collect::<Vec<_>>()
        .chunks(width)
        .map(|chunk| chunk.iter().collect::<String>())
        .collect()
}

fn sanitize_text(value: &str) -> String {
    value
        .chars()
        .map(|ch| {
            if ch.is_ascii_graphic() || ch == ' ' {
                ch
            } else if ch.is_ascii_whitespace() {
                ' '
            } else {
                '?'
            }
        })
        .collect::<String>()
        .split_whitespace()
        .collect::<Vec<_>>()
        .join(" ")
}

fn append_fill_rect(content: &mut String, x: f32, top: f32, width: f32, height: f32, gray: f32) {
    let y = PAGE_HEIGHT - top - height;
    content.push_str(&format!(
        "{gray:.3} g\n{} {} {} {} re f\n",
        fmt_num(x),
        fmt_num(y),
        fmt_num(width),
        fmt_num(height)
    ));
}

fn append_stroke_rect(
    content: &mut String,
    x: f32,
    top: f32,
    width: f32,
    height: f32,
    gray: f32,
    line_width: f32,
) {
    let y = PAGE_HEIGHT - top - height;
    content.push_str(&format!(
        "{gray:.3} G\n{} w\n{} {} {} {} re S\n",
        fmt_num(line_width),
        fmt_num(x),
        fmt_num(y),
        fmt_num(width),
        fmt_num(height)
    ));
}

fn append_stroke_line(
    content: &mut String,
    x1: f32,
    top1: f32,
    x2: f32,
    top2: f32,
    gray: f32,
    line_width: f32,
) {
    let y1 = PAGE_HEIGHT - top1;
    let y2 = PAGE_HEIGHT - top2;
    content.push_str(&format!(
        "{gray:.3} G\n{} w\n{} {} m\n{} {} l\nS\n",
        fmt_num(line_width),
        fmt_num(x1),
        fmt_num(y1),
        fmt_num(x2),
        fmt_num(y2)
    ));
}

fn append_text(
    content: &mut String,
    x: f32,
    top: f32,
    font: FontFace,
    size: f32,
    gray: f32,
    text: &str,
) {
    let baseline_y = PAGE_HEIGHT - top - size;
    content.push_str(&format!(
        "BT\n{gray:.3} g\n/{} {} Tf\n1 0 0 1 {} {} Tm\n({}) Tj\nET\n",
        font.resource_name(),
        fmt_num(size),
        fmt_num(x),
        fmt_num(baseline_y),
        escape_text(text)
    ));
}

fn append_text_centered(
    content: &mut String,
    center_x: f32,
    top: f32,
    font: FontFace,
    size: f32,
    gray: f32,
    text: &str,
) {
    let width = estimate_text_width(text, font, size);
    append_text(content, center_x - width / 2.0, top, font, size, gray, text);
}

fn append_text_right_aligned(
    content: &mut String,
    right_x: f32,
    top: f32,
    font: FontFace,
    size: f32,
    gray: f32,
    text: &str,
) {
    let width = estimate_text_width(text, font, size);
    append_text(content, right_x - width, top, font, size, gray, text);
}

fn estimate_text_width(text: &str, font: FontFace, size: f32) -> f32 {
    sanitize_text(text).chars().count() as f32 * size * font.width_factor()
}

fn fmt_num(value: f32) -> String {
    format!("{value:.2}")
}

fn object(id: usize, body: String) -> String {
    format!("{id} 0 obj\n{body}\nendobj\n")
}

fn stream_object(id: usize, stream: &str) -> String {
    format!(
        "{id} 0 obj\n<< /Length {} >>\nstream\n{}\nendstream\nendobj\n",
        stream.len(),
        stream
    )
}

fn escape_text(value: &str) -> String {
    sanitize_text(value)
        .replace('\\', "\\\\")
        .replace('(', "\\(")
        .replace(')', "\\)")
}
