const PDF_TITLE: &str = "TRAILING AUDIT EVIDENCE REPORT";
const PAGE_WIDTH: f32 = 612.0;
const PAGE_HEIGHT: f32 = 792.0;
const LEFT_MARGIN: f32 = 48.0;
const RIGHT_MARGIN: f32 = 48.0;
const TOP_MARGIN: f32 = 52.0;
const BOTTOM_MARGIN: f32 = 40.0;
const FOOTER_RESERVED: f32 = 28.0;
const CONTENT_WIDTH: f32 = PAGE_WIDTH - LEFT_MARGIN - RIGHT_MARGIN;
const CONTENT_BOTTOM: f32 = PAGE_HEIGHT - BOTTOM_MARGIN - FOOTER_RESERVED;
const BODY_LINE_HEIGHT: f32 = 14.0;
const TABLE_FONT_SIZE: f32 = 9.0;
const TABLE_LINE_HEIGHT: f32 = 12.0;
const SECTION_HEADER_HEIGHT: f32 = 24.0;
const CELL_PADDING_X: f32 = 6.0;
const CELL_PADDING_Y: f32 = 5.0;

pub fn export_package(package: &EvidencePackage) -> io::Result<Vec<u8>> {
    let mut normalized = package.clone();
    normalized.refresh_hash();

    let report = Report::from_package(&normalized);
    render_pdf(&report)
}

